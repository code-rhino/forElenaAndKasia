package io.zipcode.example;

public class FeelingRequest {

    private String name;
    private boolean feelingGood;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isFeelingGood() {
        return feelingGood;
    }

    public void setFeelingGood(boolean feelingGood) {
        this.feelingGood = feelingGood;
    }
}
